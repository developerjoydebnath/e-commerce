// ----------------------------------------------------------------------------
// ASSEMBLED DATA (The "Join" simulation)
// ----------------------------------------------------------------------------

export const products: Product[] = rawProducts.map((rp) => {
  // Join Categories
  const productCategories = rp.categoryIds
    .map((id) => categories.find((c) => c.id === id))
    .filter(Boolean) as Category[];

  // Join Brand
  const productBrand = brands.find((b) => b.id === rp.brandId) as Brand;

  // Join Reviews & populate Users
  const productReviews: Review[] = rawReviews
    .filter((r) => r.productId === rp.id)
    .map((r) => ({
      id: r.id,
      user: mockUsers.find((u) => u.id === r.userId) as User,
      rating: r.rating,
      createdAt: r.createdAt,
      likes: r.likes,
      images: r.images,
      comment: r.comment,
    }));

  // Join QnA & populate Users
  const productQnA: Question[] = rawQnA
    .filter((q) => q.productId === rp.id)
    .map((q) => ({
      id: q.id,
      q: q.question,
      a: q.answer ?? '',
      createdAt: q.createdAt,
      answeredBy: q.answeredById ? (mockUsers.find((u) => u.id === q.answeredById) as User) : null,
      questionedBy: mockUsers.find((u) => u.id === q.userId) as User,
    }));

  // Join Ratings
  const rawRating = rawRatings.find((r) => r.productId === rp.id);
  const totalReviews = rawRating
    ? rawRating.oneStar + rawRating.twoStar + rawRating.threeStar + rawRating.fourStar + rawRating.fiveStar
    : 0;

  const ratingsStats: RatingStats = rawRating
    ? {
        rating: rawRating.rating,
        total: totalReviews,
        oneStar: rawRating.oneStar,
        twoStar: rawRating.twoStar,
        threeStar: rawRating.threeStar,
        fourStar: rawRating.fourStar,
        fiveStar: rawRating.fiveStar,
      }
    : { rating: 0, total: 0, oneStar: 0, twoStar: 0, threeStar: 0, fourStar: 0, fiveStar: 0 };

  // Join Coupons
  const productCoupons = rp.couponIds
    ? (rp.couponIds.map((id) => coupons.find((c) => c.id === id)).filter(Boolean) as Coupon[])
    : undefined;

  // Variants are now pre-populated directly in RawProduct objects
  const productVariants = rp.variants || [];
  const primaryVariant = productVariants[0];

  return {
    ...rp,
    brand: productBrand,
    categories: productCategories,
    reviews: productReviews,
    questions: productQnA,
    ratings: ratingsStats,
    variants: productVariants,
    coupons: productCoupons,
    // Provide backwards-compatible empty arrays for colors and sizes as they were removed
    colors: [],
    sizes: [],
    price: rp.basePrice || (primaryVariant?.price ?? 0),
    sku: rp.sku ?? (primaryVariant?.sku || ''),
    inStock: rp.inStock ?? (productVariants.length > 0 ? productVariants.some((v) => v.inStock) : true),
    stockStatus:
      rp.stockStatus ??
      (productVariants.length > 0 && productVariants.some((v) => v.inStock)
        ? 'In Stock'
        : productVariants.length > 0
          ? 'Out of Stock'
          : 'In Stock'),
    image: rp.image ?? (primaryVariant?.image || ''),
    images: rp.images ?? (primaryVariant?.images || (primaryVariant?.image ? [primaryVariant.image] : [])),
  };
});

