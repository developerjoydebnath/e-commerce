export interface Brand {
  id: string;
  name: string;
  slug: string;
  image?: string;
}

export interface Category {
  id: string;
  name: string;
  slug: string;
  productCount?: number;
  image?: string;
}

export interface RatingStats {
  rating: number; // calculated average
  total: number;
  oneStar: number;
  twoStar: number;
  threeStar: number;
  fourStar: number;
  fiveStar: number;
}

export interface User {
  id: string;
  name: string;
  isVerified: boolean;
  avatar?: string;
}

export interface Review {
  id: string;
  user: User;
  rating: number;
  createdAt: string;
  likes: number;
  images: string[];
  comment?: string;
}

export interface Question {
  id: string;
  q: string;
  a: string;
  createdAt: string;
  questionedBy: User;
}

export interface Specification {
  key: string;
  value: string;
}

export interface Variant {
  id: string;
  sku: string;
  attributes: Record<string, string>;
  price: number;
  originalPrice?: number;
  stock: number;
  inStock: boolean;
  image: string;
  images?: string[];
}

export interface Coupon {
  id: string;
  name: string;
  expiresAt: string;
  quota: number;
}

export interface Warranty {
  warrantyPeriod: string;
  warrantyDetails: string;
}

export interface ReturnAndWarranty {
  title: string;
  description: string;
}

// THE ASSEMBLED PRODUCT (For UI compatibility)
export interface Product {
  id: string;
  name: string;
  slug: string;
  hasVariants: boolean;
  basePrice: number;
  originalPrice?: number;
  sku: string;
  inStock: boolean;
  stockStatus: string;
  discounted?: number; // percentage
  discount?: string | number;
  ratings: RatingStats;
  rating?: number; // legacy fallback
  reviews: Review[];
  image: string; // primary image
  images: string[]; // gallery images
  brand: Brand;
  categories: Category[];
  price: number;
  colors: string[];
  sizes: string[];
  attributes: {
    name: string;
    values: string[];
  }[];
  variants: Variant[];
  tags: string[];
  specifications: Specification[];
  questions: Question[];
  coupons?: Coupon[];
  warranty?: Warranty;
  description: string;
  returnAndWarranty?: ReturnAndWarranty[];
}

